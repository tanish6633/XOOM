function setLanguage(lang) {
    if (!translations[lang]) return;
    localStorage.setItem('language', lang);
    applyLanguage(lang);
}

function applyLanguage(lang) {
    const dict = translations[lang];
    if (!dict) return;

    // 1. Text Content Replacement by data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            el.innerText = dict[key];
        }
    });

    // 2. Fallback: Update common IDs if data-i18n is missing (Legacy support)
    const map = {
        'quick-actions-title': dict.quick_actions,
        'inventory-title': dict.inventory,
        'tasks-title': dict.tasks,

        // Add more specific ID mappings if needed
    };

    // Update <select> dropdown if on profile page
    const langSelect = document.querySelector('select[onchange^="updateSetting(\'Language\'"]');
    if (langSelect) {
        langSelect.value = lang;
    }
}

// Auto-init
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('language') || 'en';
    applyLanguage(savedLang);
});

// Global accessor
window.changeAppLanguage = setLanguage;
